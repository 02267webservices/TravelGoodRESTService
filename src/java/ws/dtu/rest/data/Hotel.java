package ws.dtu.rest.data;

@javax.xml.bind.annotation.XmlRootElement()
public class Hotel {
    private String name;
    private String id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
